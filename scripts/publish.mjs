// scripts/publish.mjs
//
// Syncs the public-facing subset of this repo into a sibling folder
// (default: ../noadstools-public/) for publishing to the public GitHub repo
// (Dan512/NoAdsTools), which Cloudflare Pages serves.
//
// Excludes private project-management docs (CLAUDE.md, .impeccable.md,
// docs/, .claude/) so they never reach the public repo or its history.
//
// Tool directories are driven by shared/tools.js: every status:'live' tool's
// directory is included (a missing dir for a live tool is a HARD ERROR — see
// TOOL_DIRS below), so adding a tool to the platform = flipping its manifest
// status once its directory exists.
//
// Usage:
//   node scripts/publish.mjs                              # uses default dest
//   PUBLIC_PATH=/some/other/path node scripts/publish.mjs # custom dest
//   node scripts/publish.mjs --dry-run                    # preview, no writes

import { rm, mkdir, copyFile, readdir, stat, writeFile } from 'node:fs/promises';
import { existsSync } from 'node:fs';
import { join, dirname, relative, resolve } from 'node:path';
import { pathToFileURL } from 'node:url';
import { TOOLS } from '../shared/tools.js';

const SRC = process.cwd();
const DEST = resolve(process.env.PUBLIC_PATH ?? join(SRC, '..', 'noadstools-public'));
const DRY = process.argv.includes('--dry-run');
// Cloudflare Pages rejects any single file over 25 MiB.
const MAX_FILE_BYTES = 25 * 1024 * 1024;

// ---------------------------------------------------------------------------
// What gets published. Anything not in this list is excluded.
// Directories end with '/'. Files don't.
// ---------------------------------------------------------------------------
// Manifest-driven tool dirs: a missing dir for a status:'live' tool is an
// ERROR (the homepage links it and the wipe would delete it from the public
// repo) — fail loudly instead of skipping. Static INCLUDE entries keep the
// benign skip-missing behavior.
const TOOL_DIRS = new Set(TOOLS.filter(t => t.status === 'live').map(t => `${t.slug}/`));

